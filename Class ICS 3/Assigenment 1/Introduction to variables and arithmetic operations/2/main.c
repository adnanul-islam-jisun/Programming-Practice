#include <stdio.h>
#include <stdlib.h>
#include<math.h>

int main()
{
    int a=18;
    int b=7;

    printf("%d +  %d=%d \n",a,b,a+b);
    printf("%d -  %d=%d \n",a,b,a-b);
    printf("%d *  %d=%d\n",a,b,a*b);
    printf("%d /  %d=%f \n",a,b,a/(float)b);
    printf("%d %%  %d=%d \n",a,b,a%b);


    return 0;
}
